<i style="font-size: xx-large;font-weight:bold">2005: Early Recognition on Fame Gurukul</i>

Arijit Singh first gained national recognition in 2005 when he participated in the reality
show "Fame Gurukul." Although he didn't win, the exposure marked the beginning of his journey.
<i style="font-size: xx-large;font-weight:bold">2011: Breakthrough with "Phir Mohabbat"</i>

The haunting melody "Phir Mohabbat" in "Murder 2" (2011), composed by Mithoon, marked Arijit's
breakthrough in the music industry.
<i style="font-size: xx-large;font-weight:bold">2013: "Tum Hi Ho" and Stardom</i>

The soul-stirring "Tum Hi Ho" from "Aashiqui 2" (2013) catapulted Arijit to stardom. The song
became an anthem of love, and Arijit's rendition garnered widespread acclaim.

<i style="font-size: xx-large;font-weight:bold">Diverse Collaborations and Genre Mastery</i>

Arijit demonstrated his versatility by collaborating with various composers like Pritam,
A.R. Rahman, and Jeet Gannguli. His ability to traverse genres seamlessly,
from soulful ballads to peppy numbers, established him as a versatile playback singer.

<i style="font-size: xx-large;font-weight:bold">Awards and Accolades</i>

Over the years, Arijit Singh has been honored with numerous awards, including multiple
Filmfare Awards, a National Film Award for "Tum Hi Ho," and recognition at international
events like the Global Indian Music Academy (GiMA) Awards.

<i style="font-size: xx-large;font-weight:bold">Beyond Bollywood: Independent Releases</i>

Arijit explored his artistic range beyond Bollywood with independent releases such as
 "Raabta" and "Duaa," showcasing a different dimension of his musical expression.

 <i style="font-size: xx-large;font-weight:bold">Continued Impact and Legacy</i>

Arijit Singh's impact on the Indian music scene continues to be profound, with his
voice becoming synonymous with emotional depth and musical brilliance. His legacy
is not just in the awards but in the enduring emotional connection he forms with his audience.
